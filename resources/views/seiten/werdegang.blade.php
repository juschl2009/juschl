    <h1>Werdegang</h1>
    <p>Nach 3 Jahren zuhause entschloss ich mich, den katholischen Kindergarten in Albaxen unsicher zu machen. Das gelang mir auch recht gut, so dass ich mit 6 Jahren dann zur
    katholischen Grundschule in Albaxen wechseln durfte. Hier bekam ich nach 4 Jahren dann die gymnasialeempfehlung. </p>
    <p>Auf dem König-Wilhelm-Gymnasium in Höxter konnte ich die geforderte Leistung nicht erbringen und bin dann nach 2 Jahren Stress, Tränen, blauen Briefen & mit Aussicht auf
        "Sitzenbleiben" auf die <b>Hoffmann-von-Fallersleben Realschule in Höxter</b> gewechselt.</p>
    <p>Schulform, Lehrer, Mitschüler - alles sagte mir auf Anhieb mehr zu. Praktischer Untericht und ein aufschlussreiches Schulpraktikum im Kindergarten, ein Klassenverbund
        wie er mir auf dem Gymnasium gefehlt hatte und Lehrer die einen förderten und nicht nur foderten. Mit 16 holte ich mir meinen <b>Realschulabschluss</b> ab und hatte,
        gemeinsam mit einer Klassenkameradin, die Ehre die Ablussrede halten zu dürfen.</p>
    <p>Mit diesem Abschluss in der Tasche ging's ab auf das <b>Berufskolleg in Höxter um mein Fachabitur mit Schwerpunkt Wirtschaft</b> zu machen. 2 sehr lustige und lehrreiche Jahre,
    mit Spanisch-Unterricht, Rechnungswesen, 10-Finger-Textverarbeitung & ersten kleinen Schritten in der Programmierung gingen viel zu schnell zuende. Nun war es auch endlich
        an der Zeit den Ernst des Lebens zu beginnen und vielleicht Flügge zu werden. Nach einem Praktikum im Büro meiner Tante in Berlin um mein Fachabitur zu vervollständigen,
        entschloss ich mich, mich auch in Berlin nach Ausbildungsplätzen umzuschauen.
    </p>
    <p>Nach einer Infoveranstaltung in Berlin war für mich schnell klar, dass die <b>AUCOTEAM Berufsfachschule in Berlin</b> der Ausbildungsplatz meiner Wahl war.....
        Privat, Teuer :(, <b>Assistentin für Medientechnik</b> :)<br>
    Gelernt habe ich vieles in vielen Bereichen: Tontechnik, Filmschnitt, Webdesign, Bildbearbeitung, CAD. Viel, aber nichts im Detail.</p>
    <p>Die Zeit in Berlin hat mir aber noch mehr gegeben: Unabhängigkeit, Lebenserfahrung, Aufgeschlossenheit, Freunde</p>
    <p>Nach der 3-jährigen Ausbildung habe ich noch <b>1,5 Jahre in Berlin gearbeitet</b>, bis mich die fixe Idee ereilte zu studieren und wieder näher in die Heimat zu wollen.</p>
    <p>Also bewarb ich mich um ein Studium der <b>Medienproduktion in an der TH OWL in Lemgo</b>. Nach einem Semester war klar, dass das nicht das Richtige für mich ist, ob der
        Studiengang an sich oder ich einfach nicht bereit war für ein Studium kann ich nicht genau sagen. Nach wenigen Monaten zuhause fand ich einen Ausbildungsplatz als
        <b>Fachinformatikerin für Anwendungentwicklung bei der HS3-Hotelsoftware in Detmold</b>. Die Berufsschule war das <b>Dietrich-Bonhöffer-Berufskolleg</b> ebenfalls <b>in Detmold</b>. Hier habe ich
    viel gelernt und die Leidenschaft für's Programmieren und für Webdesign entwickelt. Leider wurde ich nach meiner Ausbildung nicht übernommen und habe mich danach kurzzeitig mit
        einem Job über Wasser gehalten, bis ich dann mein zweites Studium an der <b>TH OWL in Höxter</b> anfing.</p>
    <p>Diesmal <b>Angewandte Informatik</b>, was mir sichtlich besser lag, vielleicht auch
    der Zeitpunkt besser passte. Bis zum 5. Semester, dann brach alles etwas ein, in Regelstudienzeit war der Studiengang für mich nicht mehr zu schaffen, womit das Bafög wegfiel und
    ich neben dem Studium Arbeiten musste, was mich schlicht überforderte. Nach einem Jahr Studiumspause, in der ich mir Geld auf'm Bau verdiente, war der Antrieb dahin und ich brach
    nach 7 Semestern das Studium ab. Trotz allem habe ich sehr viel mitgenommen aus dem Studium.</p>
    <p>Zu meinem Glück suchte die <b>Angel Domäne</b> zu diesem Zeitpunkt eine <b>Mediengestalterin</b>, ich bewarb mich und sie nahmen mich. </p>
